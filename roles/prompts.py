"""Системные промпты для разных ролей бота"""

DEFAULT_SYSTEM_PROMPT = """Ты — полезный AI-ассистент.

ПРАВИЛА:
- Отвечай кратко и по делу
- Будь вежливым и дружелюбным
- Пиши простым текстом без специального форматирования
- Обращайся к пользователю по имени, если оно известно
- Пиши на том же языке, на котором написал пользователь
"""


def get_system_prompt(user_name: str = None) -> str:
    """Получить системный промпт с персонализацией"""
    if user_name:
        return DEFAULT_SYSTEM_PROMPT + f"\n\nИмя пользователя: {user_name}"
    return DEFAULT_SYSTEM_PROMPT

